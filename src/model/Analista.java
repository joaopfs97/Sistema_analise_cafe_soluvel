package model;

public class Analista {
    private int registroAnalista;
    private String nomeAnalista;
    private String turnoAnalista;

    public int getRegistroAnalista() {
        return registroAnalista;
    }

    public void setRegistroAnalista(int registroAnalista) {
        this.registroAnalista = registroAnalista;
    }

    public String getNomeAnalista() {
        return nomeAnalista;
    }

    public void setNomeAnalista(String nomeAnalista) {
        this.nomeAnalista = nomeAnalista;
    }

    public String getTurnoAnalista() {
        return turnoAnalista;
    }

    public void setTurnoAnalista(String turnoAnalista) {
        this.turnoAnalista = turnoAnalista;
    }
    
    
}
