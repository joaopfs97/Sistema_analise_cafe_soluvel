package model;

public class Amostra {
    private int codigoAmostra;
    private int codigoQualidade;
    private String nomeAmostra;
    private String tipoDoCafe;

    public int getCodigoAmostra() {
        return codigoAmostra;
    }

    public void setCodigoAmostra(int codigoAmostra) {
        this.codigoAmostra = codigoAmostra;
    }

    public int getCodigoQualidade() {
        return codigoQualidade;
    }

    public void setCodigoQualidade(int codigoQualidade) {
        this.codigoQualidade = codigoQualidade;
    }

    public String getNomeAmostra() {
        return nomeAmostra;
    }

    public void setNomeAmostra(String nomeAmostra) {
        this.nomeAmostra = nomeAmostra;
    }

    public String getTipoDoCafe() {
        return tipoDoCafe;
    }

    public void setTipoDoCafe(String tipoDoCafe) {
        this.tipoDoCafe = tipoDoCafe;
    }
    
    
}
